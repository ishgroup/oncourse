package ish.oncourse.admin.services.ntis;

public class NTISException extends Exception {

	public NTISException() {
		super();
	}

	public NTISException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NTISException(String arg0) {
		super(arg0);
	}

	public NTISException(Throwable arg0) {
		super(arg0);
	}
}
